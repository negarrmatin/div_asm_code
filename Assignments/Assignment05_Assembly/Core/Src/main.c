int fah_to_cel_asm(int temperature)
{
  // Formula:
  // Subtract 32, then multiply by 5, then divide by 9
  // EX: (50F - 32) x 5/9 = 10C

  asm("SUB r0, %1, #32\n\t"
      "MOV r1, #5\n\t"
      "MUL r0, r0, r1\n\t"
      "MOV r2, #9\n\t"
      "SDIV %0, r0, r2"
      : "=r" (temperature)  // %0 is output variable
      : "0" (temperature)   // %1 is input variable
      : "r1", "r2", "cc"    // r1, r2 and APSR get clobbered
    );

  return temperature;
}

